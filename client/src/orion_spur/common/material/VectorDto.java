package orion_spur.common.material;

import java.util.List;

public class VectorDto 
{
	private float X;
	private float Y;

	public VectorDto(){}

	public VectorDto(float X, float Y)
	{
		this.X = X;
		this.Y = Y;
	}

	public float getX()
	{
		return X;
	}

	public float getY()
	{
		return Y;
	}
}


